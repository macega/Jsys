    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Map<Object, Object> parametros = new HashMap<>();
        parametros.put("dataInicial", dataInicialjDateChooser.getDate());
        parametros.put("dataFinal", dataFinaljDateChooser.getDate());
        parametros.put("idProdutos", ManagerString.Right(jTextArea1.getText().trim(), jTextArea1.getText().trim().length() - 1));
        parametros.put("detalhado", jCheckBoxDetalhado.isSelected());
        ReportUtils.openReport("Relatorio ", getRelatorio()[0], parametros);
    }//GEN-LAST:event_jButton2ActionPerformed

