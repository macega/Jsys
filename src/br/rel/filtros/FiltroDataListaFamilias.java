    public void setFiltroData() {
        if (getRelatorio()[1].equals("DIA")) {
            dataInicialjDateChooser.setDate(ManagerData.setHoraInicioDia(ManagerData.getDate()));
            dataFinaljDateChooser.setDate(ManagerData.setHoraFimDia(ManagerData.getDate()));
        }
        if (getRelatorio()[1].equals("MES")) {
            dataInicialjDateChooser.setDate(ManagerData.getPrimeiroDiaDoMes(ManagerData.getDate()));
            dataFinaljDateChooser.setDate(ManagerData.setHoraFimDia(ManagerData.getDate()));
        }
        if (getRelatorio()[1].equals("ANO")) {
            dataInicialjDateChooser.setDate(ManagerData.getPrimeiroDiaDoAno(ManagerData.getDate()));
            dataFinaljDateChooser.setDate(ManagerData.setHoraFimDia(ManagerData.getDate()));
        }
    }

    private void caregaFamilias() {
        // carega registros dos NCM
        for (Object O : br.sql.util.Retorna.findList("JsysProdutosTFamilias.findAll")) {
            br.sql.bean.JsysProdutosTFamilias fam = (br.sql.bean.JsysProdutosTFamilias) O;
            uJComboBoxFamilia.addItem(fam.getNomeFamilia() + " - " + fam.getIdFamilia(), fam);
        }
        uJComboBoxFamilia.setSelectedIndex(0);
    }

    private br.sql.bean.JsysProdutosTFamilias getuJComboBoxFamilia() {
        return (br.sql.bean.JsysProdutosTFamilias) uJComboBoxFamilia.getSelectedObject();
    }

