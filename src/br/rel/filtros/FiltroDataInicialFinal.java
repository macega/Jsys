    private void setVariaveis(String localRelatorio, int periodo, Integer idVendedor) {
        this.localRelatorio = localRelatorio;
        switch (periodo) {
            case DIA:
                setdate(ManagerData.getDataAtualTypeDate(ManagerData.INICIO), ManagerData.getDataAtualTypeDate(ManagerData.FIM));
                break;
            case MES:
                setdate(ManagerData.getPrimeiroDiaDoMes(), ManagerData.getDataAtualTypeDate(ManagerData.FIM));
                break;
            case ANO:
                setdate(ManagerData.getPrimeiroDiaDoAno(ManagerData.getDate()), ManagerData.getDataAtualTypeDate(ManagerData.FIM));
                break;
            case FIM_MES:
                setdate(ManagerData.getPrimeiroDiaDoMes(), ManagerData.getUltimoDiaDoMes());
                break;
            default:
                setdate(ManagerData.getDataAtualTypeDate(ManagerData.INICIO), ManagerData.getDataAtualTypeDate(ManagerData.FIM));
                break;
        }
        this.idVendedor = idVendedor == null ? 0 : idVendedor;
    }

    private void setdate(Date dataInicial, Date dataFinal) {
        dataInicialjDateChooser.setDate(dataInicial);
        dataFinaljDateChooser.setDate(dataFinal);
    }
