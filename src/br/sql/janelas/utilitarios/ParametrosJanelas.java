    private String getCrt(String crt) {
        switch (crt) {
            case "1":
                return "1 Simples Nacional";
            case "2":
                return "2 Simples Nacional, excesso sublimite de receita bruta";
            case "3":
                return "3 Regime Normal";
            default:
                return "1 Simples Nacional";
        }
    }

    private String getJsysSubconta(String idGeral) {
        Map<Object, Object> filtro = new HashMap<>();
        filtro.put("idGeral", idGeral);
        JsysSubConta sc = (JsysSubConta) Retorna.findOneResult("JsysSubConta.findByIdGeral", filtro);
        if (sc != null) {
            return sc.getDescricao() + " - " + sc.getIdGeral();
        } else {
            return "";
        }
    }

    private String getJsysTitulo() {
        Map<Object, Object> filtro = new HashMap<>();
        filtro.put("idTitulo", jP.getIdTituloDinhero());
        JsysTitulos t = (JsysTitulos) Retorna.findOneResult("JsysTitulos.findByIdTitulo", filtro);
        if (t != null) {
            return t.getTipoFatura() + " - " + t.getIdTitulo();
        } else {
            return "";
        }
    }

    private String getJsysIdTriburacao() {
        if (jP.getIdTributacao() != null) {
            return jP.getIdTributacao().getNome() + " - " + jP.getIdTributacao().getDecreto();
        } else {
            return "";
        }
    }

    private String getJsysClientes(Integer idCliente) {
        Map<Object, Object> filtro = new HashMap<>();
        filtro.put("idCliente", idCliente);
        JsysClientes c = (JsysClientes) Retorna.findOneResult("JsysClientes.findByIdCliente", filtro);
        if (c != null) {
            return c.getNomeCorentista() + " - " + c.getIdCliente();
        } else {
            return "";
        }
    }

