            JavaApplicationJsys.INI.setString("EMAIL", "NOME_EMAIL", nomeEmailTF.getText());
            JavaApplicationJsys.INI.setString("EMAIL", "USER", userTF.getText());
            JavaApplicationJsys.INI.setString("EMAIL", "SENHA", String.valueOf(senhajPF.getPassword()));
