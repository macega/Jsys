    @SuppressWarnings("unchecked")
    private void loadConponentes() {
        /* Scan found services to see if anyone suits our needs */
        //impressorasVendasComboBox.set
        for (PrintService p : PrintServiceLookup.lookupPrintServices(null, null)) {
            impressorasVendasComboBox.addItem(p.getName());
            impressoraNfceComboBox.addItem(p.getName());
        }
        impressorasVendasComboBox.setEditable(true);
        impressoraNfceComboBox.setEditable(true);
    }
