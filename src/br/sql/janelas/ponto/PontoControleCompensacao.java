    private void caregarGrid() {
        final Progress p = new Progress();
        SwingWorker work = new SwingWorker() {

            @Override
            protected Object doInBackground() throws Exception {
                CompencacaoTableModel model = null;
                Date dataInicial = ManagerData.getPrimeiroDiaDoMes(jMonthChooserMes.getMonth(), jYearChooserAno.getYear());
                Date dataFinal = ManagerData.getUltimoDiaDoMes(jMonthChooserMes.getMonth(), jYearChooserAno.getYear());
                try {
                    model = new CompencacaoTableModel(dataInicial, dataFinal);
                } catch (SQLException e) {
                    Log.registraErro(this.getClass().getName(), "caregarGrid.caregarGrid", e);
                }
                jTableMaster.setModel(model);
                for (int i = 0; i < jTableMaster.getColumnCount(); ++i) {
                    int t;
                    switch (i) {
                        case 0:
                            t = 45;
                            break;
                        case 1:
                            t = 260;
                            break;
                        case 2:
                            t = 50;
                            break;
                        default:
                            t = 30;
                            break;
                    }
                    jTableMaster.getColumnModel().getColumn(i).setPreferredWidth(t);
                    if (i > 2) {
                        jTableMaster.getColumnModel().getColumn(i).setCellRenderer(new br.sql.defaultTableCellRenderer.Compencacao());
                    }
                }
                return null;
            }

            @Override
            protected void done() {
                super.done();
                p.setVisible(false);
            }
        };
        work.execute();
        p.setVisible(true);

    }

    private void setCompensacao() {
        jTableMaster.setValueAt(1, jTableMaster.getSelectedRow(), jTableMaster.getSelectedColumn());
    }
