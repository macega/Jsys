    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (validar()) {
            final Progress p = new Progress();
            SwingWorker worker = new SwingWorker() {
                @Override
                protected Void doInBackground() throws Exception {
                    ajustarPonto();
                    return null;
                }

                @Override
                protected void done() {
                    super.done();
                    p.dispose();
                    JOptionPane.showMessageDialog(null, "Processo Realizado com Sucesso!", "Mensagem", JOptionPane.INFORMATION_MESSAGE);
                }

                private void ajustarPonto() {
                    Integer idFuncionario = ((JsysFuncionarios) uJComboBoxColaborador.getSelectedObject()).getIdFuncionario();
                    Date dateInicial = ManagerData.setHoraInicioDia(jDateChooserInicio.getDate());
                    Date dateFinal = ManagerData.setHoraInicioDia(jDateChooserFim.getDate());
                    for (Date date : ManagerData.getList(dateInicial, dateFinal)) {
                        StringBuilder sql = new StringBuilder();
                        sql.append("AjustePonto(").append(idFuncionario).append(", ")
                                .append("'").append(ManagerData.convertUsDate(date)).append("', ")
                                .append("'").append(uJComboBoxObs.getSelectedItem()).append("')");
                        DADOS.execProcedure(sql.toString());
                    }
                }
            };
            worker.execute();
            p.setVisible(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

