     private void caregarDados() {
        for (Object O : Retorna.findList("JsysFuncionarios.findAll")) {
            JsysFuncionarios c = (JsysFuncionarios) O;
            uJComboBoxColaborador.addItem(c.getNomeCliente(), c);
        }
    }
