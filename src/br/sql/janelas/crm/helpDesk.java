    public helpDesk(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        setTitle("Help Desk");
        initComponents();
        List<Object> O = Retorna.findList("HelpDeskTipos.findAll");
        for (Object o : O) {
            HelpDeskTipos h = (HelpDeskTipos) o;
            uJComboBoxtipo.addItem(h.getTipo(), h);
        }
        for (JsysLojas l : Retorna.jsysLojas()) {
            uJComboBoxLoja.addItem(l.getNomeLoja() + " - " + l.getCnpj(), l);
        }
        Map<Object, Object> filtro = new HashMap<>();
        filtro.put("ativo", true);
        JsysLojas jL = ((JsysLojas) Retorna.findOneResult("JsysLojas.findByAtivo", filtro));
        uJComboBoxLoja.setSelectedItem(jL.getNomeLoja() + " - " + jL.getCnpj());
        jTextFieldUsuario.setText(Menu.getNomeUsuario());
    }

