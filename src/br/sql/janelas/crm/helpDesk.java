    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        HelpDesk H = new HelpDesk();
        H.setData(ManagerData.getDate());
        H.setFechado(false);
        H.setIdLoja(((JsysLojas) uJComboBoxLoja.getSelectedObject()).getIdloja());
        H.setObs(jTextArea1.getText().toLowerCase().trim());
        H.setUsuario(jTextFieldUsuario.getText());
        H.setTipo(((HelpDeskTipos) uJComboBoxtipo.getSelectedObject()).getTipo());
        if (br.sql.acesso.ConnectionFactory.insert(H) instanceof HelpDesk) {
            JOptionPane.showMessageDialog(null, "Chamado Criado com Sucesso");
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Erro Ao Criar o Chamado");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

