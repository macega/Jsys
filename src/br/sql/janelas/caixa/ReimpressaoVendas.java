    private void mostraVenda() {
        jTextFieldData.setText("");
        jTextFieldVendedor.setText("");
        jTextFieldValor.setText("R$ 0.00");
        Map<Object, Object> filtro = new HashMap<>();
        filtro.put("idOrcamento", getIdVenda());
        br.sql.bean.JsysOrcamento O = (br.sql.bean.JsysOrcamento) br.sql.util.Retorna.findOneResult("JsysOrcamento.findByIdOrcamento", filtro);
        if (O != null) { //  !Objects.equals(O, null)
             jTextFieldData.setText(ManagerData.convertDate(O.getData(), "dd/MM/yyyy"));
            jTextFieldVendedor.setText(O.getIdFuncionario().getIdCliente() + " - " + O.getIdFuncionario().getNomeCorentista());
            jTextFieldValor.setText(ManagerDecimal.converter(O.getValorLiquido(), "R$ #,##0.00"));
        }
    }
