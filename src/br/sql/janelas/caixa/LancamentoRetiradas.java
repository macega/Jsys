    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Map<Object, Object> filtro = new HashMap<>();
        filtro.put("data", ManagerData.getDataAtualTypeDate(ManagerData.INICIO));
        filtro.put("idBancoDestino", Retorna.jsysClientes(Menu.getIdBanco())); // new JsysClientes(Constantes.ID_BANCO)
        filtro.put("idGeral", PAR.getIdGeralAberturaCaixa());
        JsysTranferenciaEntreContas abertura = (JsysTranferenciaEntreContas) Retorna.findOneResult("JsysTranferenciaEntreContas.findByAberturaDodia", filtro);
        if (abertura != null) {
            if (ManagerDecimal.StringToDouble(jTextField4.getText()) > 0) {
                try {
                    JsysTranferenciaEntreContas T = new JsysTranferenciaEntreContas();
                    T.setIdGeral(PAR.getIdGeralRetiradas());
                    T.setValor(ManagerDecimal.StringToBigDecimal(jTextField4.getText()));
                    T.setData(ManagerData.setHoraInicioDia(ManagerData.getDate()));
                    T.setDescricao("");
                    T.setIdBancoOrigem(Retorna.jsysClientes(Menu.getIdBanco()));
                    T.setIdBancoDestino(Retorna.jsysClientes(PAR.getIdBancoCofre()));
                    T.setCancelada(false);
                    T.setIdTitulo(((JsysTitulos) jComboBox4.getSelectedItem()).getIdTitulo());
                    T.setUsuarioInclusao(Menu.getNomeUsuario());
                    T.setDataInclusao(ManagerData.getDate());
                    T.setRetirado(false);
                    T.setDescRetirado("");
                    ConnectionFactory.insert(T);
                    JOptionPane.showMessageDialog(null, "Retirada Realizada com Sucesso", "Confirma", JOptionPane.INFORMATION_MESSAGE);
                    jTextField4.setText("");
                    jTextField4.requestFocus();
                } catch (RollbackException e) {
                    Log.registraErro(this.getClass().getName(), "jButton2ActionPerformed", e);
                    //ConnectionFactory.getEntityManagerNew().getTransaction().begin();
                    erro("Erro Retirada não Realizada");
                }
            } else {
                erro("Erro no Valor");
            }
        } else {
            erro("O caixa ainda não esta aberto.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

