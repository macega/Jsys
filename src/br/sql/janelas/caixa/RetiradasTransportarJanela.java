    @SuppressWarnings("unchecked")
    private void refreshButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreshButtonActionPerformed
        ConnectionFactory.cancelar();
        java.util.Collection data = query.getResultList();
        for (Object entity : data) {
            ConnectionFactory.getEntityManagerNew().refresh(entity);
        }
        list.clear();
        list.addAll(data);
        totalRetirada();
    }//GEN-LAST:event_refreshButtonActionPerformed

    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        try {
            int i = masterTable.getSelectedRow() + 1;
            masterTable.changeSelection(i, 0, true, true);
            ConnectionFactory.salvar();
            JOptionPane.showMessageDialog(this, "Salvo com Sucesso", "Aviso", JOptionPane.INFORMATION_MESSAGE);
        } catch (RollbackException ex) {
            Log.registraErro(RetiradasTransportarJanela.class, "saveButtonActionPerformed", ex);
            JOptionPane.showMessageDialog(this, "Nao foi possivel Salvar", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        refreshButtonActionPerformed(null);
    }//GEN-LAST:event_saveButtonActionPerformed

