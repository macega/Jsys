    public void totalRetiradaMarcadas() {
        BigDecimal sum = BigDecimal.ZERO;
        for (int i = 0; i < masterTable.getRowCount(); i++) {
            if (masterTable.getValueAt(i, 2).equals(true)) {
                sum = sum.add((BigDecimal) masterTable.getValueAt(i, 3));
            }
        }
        retiradasMarcadasTF.setValue(sum);
    }

    public void totalRetirada() {
        BigDecimal sum = BigDecimal.ZERO;
        for (int i = 0; i < masterTable.getRowCount(); i++) {
            sum = sum.add((BigDecimal) masterTable.getValueAt(i, 3));
        }
        totalRetiradasTF.setValue(sum);
    }

    @SuppressWarnings("unchecked")
    private void setFiltroData() {
        Map<Object, Object> filtro = new HashMap<>();
        java.util.Collection retiradas = null;
        if (jRadioButtonTodos.isSelected()) {
            filtro.clear();
            filtro.put("dataInicial", dataInicioJDC.getDate());
            filtro.put("dataFinal", dataFimJDC.getDate());
            retiradas = Retorna.findCollection("Retiradas.findByDataBetween", filtro);
        } else if (jRadioButtonPendentes.isSelected()) {
            filtro.clear();
            filtro.put("dataInicial", dataInicioJDC.getDate());
            filtro.put("dataFinal", dataFimJDC.getDate());
            filtro.put("retirado", false);
            retiradas = Retorna.findCollection("Retiradas.findByDataBetweenRetirado", filtro);
        } else if (jRadioButtonConfirmados.isSelected()) {
            filtro.clear();
            filtro.put("dataInicial", dataInicioJDC.getDate());
            filtro.put("dataFinal", dataFimJDC.getDate());
            filtro.put("retirado", true);
            retiradas = Retorna.findCollection("Retiradas.findByDataBetweenRetirado", filtro);
        } else if (jRadioButtonTodosPendentes.isSelected()) {
            retiradas = Retorna.findCollection("Retiradas.findAllPendentes");
        }
        list.clear();
        list.addAll(retiradas);
    }
