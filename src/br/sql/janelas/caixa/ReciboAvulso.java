    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        ReciboBean recibo = new ReciboBean();
        recibo.setCnpjCpf(jTextFieldCnpjCpf.getText());
        recibo.setEmitente(jTextFieldEmitente.getText());
        recibo.setEndereco(jTextFieldEndereco.getText());
        recibo.setQuantiaDe(ManagerDecimal.valorExtenso(ManagerDecimal.StringToBigDecimal(jTextFieldValor.getText())));
        recibo.setRecebeDe(jTextFieldRecebeDe.getText());
        recibo.setReferenteA(jTextFieldReferenteA.getText());
        recibo.setValor(ManagerDecimal.StringToDouble(jTextFieldValor.getText()));
        recibo.setParFantasia(par.getFantasia());
        StringBuilder parEndereco = new StringBuilder();
        parEndereco.append("Fone: ").append(par.getFone()).append(System.lineSeparator());
        parEndereco.append("CNPJ: ").append(par.getCnpj()).append(" Inscri√ß√£o Estadual: ").append(par.getInscricao()).append(System.lineSeparator());
        parEndereco.append(par.getEndereco()).append(", ").append(par.getNumero()).append(" - ").append(par.getBairro()).append(" - ").append(par.getCidade()).append(" - ").append(par.getUf());
        recibo.setParEndereco(parEndereco.toString());
        recibo.setParLogo(par.getLogo());
        ArrayList<ReciboBean> dadosRecibo = new ArrayList<>();
        dadosRecibo.add(recibo);
        ReportUtils.openReport("Recibo Avulso", "/br/rel/caixa/ReciboAvulso.jasper", new JRBeanCollectionDataSource(dadosRecibo));
    }//GEN-LAST:event_jButton2ActionPerformed

