    public void setPagamento(Pagamentos pagamento) {
        this.pagamento = pagamento;
        if (this.pagamento == null) {
            jTextFieldData.setText("");
            jTextFieldFornecedor.setText("");
            jTextFieldCaixa.setText("");
            jTextFieldTipoPagamento.setText("");
            jTextFieldValor.setText("R$ 0,00");
        } else {
            jTextFieldData.setText(ManagerData.convertBrDate(pagamento.getDataEmissao()));
            jTextFieldFornecedor.setText(pagamento.getNomeFornecedor());
            jTextFieldCaixa.setText(pagamento.getUsuarioInclusao());
            jTextFieldTipoPagamento.setText(Retorna.pagamentos(pagamento.getIdContabil()).getDescricao());
            jTextFieldValor.setText(ManagerDecimal.converter(pagamento.getValorTitulo()));
        }
    }

