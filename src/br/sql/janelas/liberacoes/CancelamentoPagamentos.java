    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (pagamento != null) {
            LiberacaoGenericaJDialog L = new LiberacaoGenericaJDialog(
                    LiberacaoGenericaJDialog.FINANCEIRO,
                    "Pagamentos",
                    pagamento.getIdPagamentos().toString(),
                    LiberacaoGenericaJDialog.CANCELAMENTO,
                    "");
            if (L.isConfirma()) {
                pagamento.setValorEstorno(pagamento.getValorPago());
                pagamento.setDataAlteracao(ManagerData.getDate());
                pagamento.setUsuarioAlteracao(Menu.getNomeUsuario());
                pagamento.setDataCancelar(ManagerData.getDate());
                pagamento.setObsCancelamento(L.getMotivo());
                pagamento.setQuitado(false);
                pagamento.setIdTitulo("");
                if (ConnectionFactory.update(pagamento) != null) {
                    JOptionPane.showMessageDialog(this, "Pagamento Cancelado", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao cancelar pagamento", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this,
                        "Acesso não Autorizado."
                        + System.lineSeparator()
                        + "Retirada não Encontrada.",
                        "Erro",
                        JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTextFieldIdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldIdKeyReleased
        try {
            if (jTextFieldId.getText().length() > 0) {
                Map<Object, Object> filtro = new HashMap<>();
                filtro.put("idPagamentos", Integer.parseInt(jTextFieldId.getText()));
                setPagamento((Pagamentos) Retorna.findOneResult("Pagamentos.findByIdPagamentos", filtro));
            } else {
                setPagamento(null);
            }
        } catch (NumberFormatException e) {
            setPagamento(null);
            Log.registraErro(this.getClass().getName(), "jTextFieldIdKeyReleased", e);
        }
    }//GEN-LAST:event_jTextFieldIdKeyReleased

