    private void idVendaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idVendaKeyReleased
        setRetornoId(0);
        jTextFieldData.setText("");
        jTextFieldVendedor.setText("");
        jTextFieldValor.setText("R$ 0.00");
        Map<Object, Object> filtro = new HashMap<>();
        filtro.put("id", Integer.parseInt(idVenda.getText()));
        JsysOrcamentoReabertura ro = (JsysOrcamentoReabertura) Retorna.findOneResult("JsysOrcamentoReabertura.findById", filtro);
        if (ro != null) {
            setRetornoId(ro.getId());
            jTextFieldData.setText(ManagerData.convertDate(ro.getData(), "dd/MM/yyyy"));
            jTextFieldVendedor.setText(ro.getIdVendedor() + " - " + ro.getVendedor());
            jTextFieldValor.setText(ManagerDecimal.converter(ro.getValorLiquido(), "R$ #,##0.00"));
        }
    }//GEN-LAST:event_idVendaKeyReleased

