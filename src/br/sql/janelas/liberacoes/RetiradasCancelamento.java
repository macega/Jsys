    private void idTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idTextFieldKeyReleased
        Map<Object, Object> filtro = new HashMap<>();
        filtro.put("id", Long.valueOf(idTextField.getText()));
        filtro.put("idGeral", par.getIdGeralRetiradas());
        retirada = (JsysTranferenciaEntreContas) Retorna.findOneResult("JsysTranferenciaEntreContas.findByIdandIdGeral", filtro);
        if (retirada != null) {
            datajTextField.setText(ManagerData.convertBrDate(retirada.getData()));
            obsjTextField.setText(new StringBuilder()
                    .append(retirada.getIdBancoOrigem().getIdCliente())
                    .append(" ")
                    .append(retirada.getIdBancoOrigem().getNomeCorentista())
                    .append(" Valor: ").append(ManagerDecimal.converter(retirada.getValor()))
                    .append("").toString());
        }
    }//GEN-LAST:event_idTextFieldKeyReleased

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        LiberacaoGenericaJDialog lg = new LiberacaoGenericaJDialog(LiberacaoGenericaJDialog.GERENCIA,
                "JsysTranferenciaEntreContas",
                retirada.getId().toString(),
                LiberacaoGenericaJDialog.CANCELAMENTO,
                "");
        if (lg.isConfirma() && retirada != null) {
            retirada.setCancelada(true);
            retirada.setDataAlteracao(ManagerData.getDate());
            retirada.setUsuarioAlteracao(Menu.getNomeUsuario());
            ConnectionFactory.update(retirada);
            if (retirada instanceof JsysTranferenciaEntreContas
                    & retirada != null) {
                JOptionPane.showMessageDialog(this, "Cancelamento efetuado com sucesso.", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao cancelar retirada", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Acesso não Autorizado." + System.lineSeparator() + "Retirada não Encontrada.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

