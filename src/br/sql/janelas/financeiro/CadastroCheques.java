    private void setFiltro() {
        Map<Object, Object> filtro = new HashMap<>();
        List<Object> objeto;
        if (jTextFieldNumeroChequeFiltro.getText().isEmpty()) {
            if (jCheckBoxTodosFiltro.isSelected()) {
                if (jDateChooserInicialFiltro.getDate() != null) {
                    filtro.clear();
                    filtro.put("dataVencimentoInicial", ManagerData.setHoraInicioDia(jDateChooserInicialFiltro.getDate()));
                    filtro.put("dataVencimentoFinal", ManagerData.setHoraInicioDia(jDateChooserFinalFiltro.getDate()));
                    filtro.put("quitado", jCheckBoxQuitadoFiltro.isSelected());
                    objeto = Retorna.findList("JsysCheques.findByDataVencimentoAndQuitado", filtro);
                } else if (jCheckBoxDevolvidos.isSelected()) {
                    filtro.clear();
                    filtro.put("devolvido", jCheckBoxDevolvidos.isSelected());
                    objeto = Retorna.findList("JsysCheques.findByDevolvido", filtro);
                } else {
                    filtro.clear();
                    filtro.put("quitado", jCheckBoxQuitadoFiltro.isSelected());
                    objeto = Retorna.findList("JsysCheques.findByQuitado", filtro);
                }
            } else {
                if (jDateChooserInicialFiltro.getDate() != null) {
                    filtro.clear();
                    filtro.put("idCliente", (JsysClientes) uJComboBoxcedenteFiltro.getSelectedObject());
                    filtro.put("dataVencimentoInicial", ManagerData.setHoraInicioDia(jDateChooserInicialFiltro.getDate()));
                    filtro.put("dataVencimentoFinal", ManagerData.setHoraInicioDia(jDateChooserFinalFiltro.getDate()));
                    filtro.put("quitado", jCheckBoxQuitadoFiltro.isSelected());
                    objeto = Retorna.findList("JsysCheques.findByIdClienteDataVencimentoAndQuitado", filtro);
                } else if (jCheckBoxDevolvidos.isSelected()) {
                    filtro.clear();
                    filtro.put("idCliente", (JsysClientes) uJComboBoxcedenteFiltro.getSelectedObject());
                    filtro.put("devolvido", jCheckBoxDevolvidos.isSelected());
                    objeto = Retorna.findList("JsysCheques.findByIdClienteDevolvido", filtro);
                } else {
                    filtro.clear();
                    filtro.put("idCliente", (JsysClientes) uJComboBoxcedenteFiltro.getSelectedObject());
                    filtro.put("quitado", jCheckBoxQuitadoFiltro.isSelected());
                    objeto = Retorna.findList("JsysCheques.findByIdClienteQuitado", filtro);
                }
            }
        } else {
            filtro.clear();
            filtro.put("numeroCheque", jTextFieldNumeroChequeFiltro.getText().trim());
            objeto = Retorna.findList("JsysCheques.findByNumeroCheque", filtro);
        }
        list.clear();
        for (Object o : objeto) {
            JsysCheques ch = (JsysCheques) o;
            list.add(ch);
        }
        BigDecimal titulos = BigDecimal.ZERO;
        for (int i = 0; i < masterTable.getRowCount(); i++) {
            titulos = titulos.add((BigDecimal) masterTable.getValueAt(i, 6));
        }
        jTextFieldTotalCheques.setText(br.sql.util.ManagerDecimal.converter(titulos));
        jTextFieldRegistros.setText(String.valueOf(masterTable.getRowCount()));
    }

    private JsysCheques getRegistroSelecionado() {
        int[] selected = masterTable.getSelectedRows();
        JsysCheques cheque = null;
        for (int idx = 0; idx < selected.length; idx++) {
            Map<Object, Object> filtro = new HashMap<>();
            filtro.put("idcheque", (Integer) masterTable.getValueAt(selected[idx], 0));
            cheque = (JsysCheques) Retorna.findOneResult("JsysCheques.findByIdcheque", filtro);
        }
        return cheque;
    }

    class devolucao extends SwingWorker<Void, Void> {

        private final JsysCheques cheque;
        private final Progress p;

        public devolucao(JsysCheques cheque, Progress p) {
            this.cheque = cheque;
            this.p = p;
        }

        @Override
        protected Void doInBackground() {
            Liberacao l = new Liberacao();
            l.setTabelaOrigem("jsysCheques");
            l.setIdOrigem(cheque.getIdcheque().toString());
            l.setMotivo("Devolução de Cheques");
            l.setUsuario(Menu.getNomeUsuario());
            l.setData(ManagerData.getDataAtualTypeDate(ManagerData.INICIO));
            l.setHora(ManagerData.getDate());
            l.setTipo(LiberacaoGenericaJDialog.DEVOLUCAO);
            l.setUsuarioLogado(Menu.getNomeUsuario());
            cheque.setCancelado(false);
            cheque.setQuitado(false);
            cheque.setDevolvido(true);
            cheque.setNumeroCompensacao("");
            cheque.setValorPago(BigDecimal.ZERO);
            cheque.setIdCaixa(0);
            try {
                ConnectionFactory.insert(l);
                ConnectionFactory.update(cheque);
            } catch (RollbackException e) {
                Log.registraErro(this.getClass().getName(), "reabrirButtonActionPerformed", e);
                p.setVisible(false);
            }
            return null;
        }

        @Override
        protected void done() {
            super.done();
            setFiltro();
            p.setVisible(false);
        }
    };
