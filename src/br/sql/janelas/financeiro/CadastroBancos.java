    private void setCidade() {
        Map<Object, Object> filtro = new HashMap<>();
        filtro.put("uf", jComboBoxUF.getSelectedItem() == null ? "RO" : jComboBoxUF.getSelectedItem().toString());
        List<Object> cidadeList = br.sql.util.Retorna.findList("Cidades.findByUf", filtro);
        try {
            uJComboBoxCidade.clear();
            for (Object O : cidadeList) {
                Cidades c = (Cidades) O;
                uJComboBoxCidade.addItem(c.getMunicipio(), c);
            }
            if (registroEditavel.getCidade().equals("")) {
                uJComboBoxCidade.setSelectedIndex(0);
            } else {
                uJComboBoxCidade.setSelectedItem(registroEditavel.getCidade());
            }
        } catch (Exception ignore) {
        }
    }
