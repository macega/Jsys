    private void jTabbedPane1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jTabbedPane1StateChanged
        int[] selection = masterTable.getSelectedRows();
        for (int i = 0; i < selection.length; i++) {
            registroEditavel = (JsysBancos) list.get(masterTable.convertRowIndexToModel(selection[i]));
        }
        if (registroEditavel != null) {
            jComboBoxUF.setSelectedItem(registroEditavel.getEstado());
            jTextFieldFone.setText(registroEditavel.getFone());
            jTextFieldNome.setText(registroEditavel.getNomeBanco());
            jTextFieldNumero.setText(registroEditavel.getNumeroBanco());
            jTextFieldDV.setText(registroEditavel.getNumeroBancoDv());
            jTextFieldConta.setText(registroEditavel.getNumeroConta());
            setCidade();
        }
    }//GEN-LAST:event_jTabbedPane1StateChanged

