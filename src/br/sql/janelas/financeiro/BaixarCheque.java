    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        cheque.setQuitado(true);
        cheque.setNumeroCompensacao(numeroCompesacaoJTextFild.getText());
        cheque.setValorPago(cheque.getValorCheque());
        cheque.setDataPagamento(jDateChooserData.getDate());
        cheque.setIdCaixa(Menu.getIdBanco());
        try {
            br.sql.acesso.ConnectionFactory.update(cheque);
            //br.sql.acesso.ConnectionFactory.comit();
        } catch (RollbackException e) {
            Log.registraErro(this.getClass().getName(), "jButton2ActionPerformed", e);
        }
        this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

