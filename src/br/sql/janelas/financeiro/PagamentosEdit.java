    private void jButtonConfirmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmaActionPerformed
        pagamento.setOutrosNumeros(jTextFieldOutrosNumeros.getText());
        pagamento.setIdLoja(((JsysLojas) uJComboBoxEmpresa.getSelectedObject()).getIdloja());
        pagamento.setIdTitulo(uJComboBoxTitulos.getSelectedItem().toString());
        pagamento.setDataEmissao(ManagerData.setHoraInicioDia(jDateChooserEmissao.getDate()));
        pagamento.setDataVencimento(ManagerData.setHoraInicioDia(jDateChooserVencimento.getDate()));
        pagamento.setDataUltimoPgto(jDateChooserEmissao.getDate());
        pagamento.setSemCadastrado(jCheckBoxSemCadastro.isSelected());
        if (jCheckBoxSemCadastro.isSelected()) {
            //JsysClientes c = Retorna.jsysClientes(par.getIdForncedor());
            pagamento.setNomeFornecedor(jTextFieldCorentista.getText());
            pagamento.setIdFornecedor(Retorna.jsysClientes(PAR.getIdForncedor()));
        } else {
            pagamento.setNomeFornecedor(((JsysClientes) uJComboBoxCorentista.getSelectedObject()).getNomeCorentista());
            pagamento.setIdFornecedor((JsysClientes) uJComboBoxCorentista.getSelectedObject());
        }
        pagamento.setIdContabil(((JsysSubConta) uJComboBoxTipoPagamentos.getSelectedObject()).getIdGeral());
        pagamento.setValorTitulo(ManagerDecimal.StringToBigDecimal(jTextFieldvalor.getText()));
        pagamento.setRestante(ManagerDecimal.StringToBigDecimal(jTextFieldvalor.getText()));
        pagamento.setMulta(ManagerDecimal.StringToBigDecimal(jTextFieldMulta.getText()));
        pagamento.setJuros(ManagerDecimal.StringToBigDecimal(jTextFieldJuros.getText()));
        pagamento.setCorrecao(ManagerDecimal.StringToBigDecimal(jTextFieldCorecao.getText()));
        pagamento.setObs(jTextAreaObs.getText());
        if (pagamento.getIdPagamentos() == null) {
            pagamento.setDataInclusao(br.sql.util.ManagerData.getDataAtualTypeDate(ManagerData.INICIO));
            pagamento.setUsuarioInclusao(Menu.getNomeUsuario());
            ConnectionFactory.insert(pagamento);
        } else {
            pagamento.setUsuarioAlteracao(Menu.getNomeUsuario());
            pagamento.setDataAlteracao(ManagerData.getDate());
            ConnectionFactory.update(pagamento);
        }
        this.dispose();
    }//GEN-LAST:event_jButtonConfirmaActionPerformed

