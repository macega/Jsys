    private void caregaDados() {
        try {
            for (JsysLojas loja : br.sql.util.Retorna.jsysLojas()) {
                uJComboBoxEmpresa.addItem(loja.getCnpj() + " - " + loja.getNomeLoja(), loja);
            }
        } catch (Exception e) {
            uJComboBoxEmpresa.setSelectedItem("Não Tem Empresas Cadastradas ...");
        }
        // carega retistros dos titulos
        try {
            for (Object O : br.sql.util.Retorna.findList("JsysTitulos.findAll")) {
                JsysTitulos t = (JsysTitulos) O;
                uJComboBoxTitulos.addItem(t.getIdTitulo(), t);
            }
            uJComboBoxTitulos.setSelectedIndex(0);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Não Foi Encontrado Títulos Cadastrados!!!");
            this.dispose();
        }
        // carega retistros dos Corentistas
        try {
            for (Object O : br.sql.util.Retorna.findList("JsysClientes.findAllAtivos")) {
                JsysClientes c = (JsysClientes) O;
                uJComboBoxCorentista.addItem(c.getNomeCorentista() + " - " + c.getIdCliente().toString(), c);
            }
            uJComboBoxCorentista.setSelectedIndex(0);
        } catch (Exception e) {
            Log.registraErro(this.getClass().getName(), "caregaDados", e);
        }
        // carega subConta 
        try {
            for (Object O : br.sql.util.Retorna.findList("JsysSubConta.findAll")) {
                JsysSubConta sc = (JsysSubConta) O;
                uJComboBoxTipoPagamentos.addItem(sc.getDescricao() + " - " + sc.getIdGeral(), sc);
            }
        } catch (Exception e) {
            Log.registraErro(this.getClass().getName(), "caregaDados", e);
        }
        // mostra dados do Registro atual
        if (pagamento != null) {
            Map<Object, Object> filtro = new HashMap<>();
            filtro.put("idGeral", pagamento.getIdContabil());
            JsysSubConta sb = (JsysSubConta) br.sql.util.Retorna.findOneResult("JsysSubConta.findByIdGeral", filtro);
            uJComboBoxTipoPagamentos.setSelectedItem(sb.getDescricao() + " - " + sb.getIdGeral());
            JsysLojas l = br.sql.util.Retorna.jsysLojas(pagamento.getIdLoja());
            uJComboBoxEmpresa.setSelectedItem(l.getCnpj() + " - " + l.getNomeLoja());
            uJComboBoxTitulos.setSelectedItem(pagamento.getIdTitulo());
            jDateChooserEmissao.setDate(pagamento.getDataEmissao());
            jDateChooserVencimento.setDate(pagamento.getDataVencimento());
            jCheckBoxSemCadastro.setSelected(pagamento.getSemCadastrado());
            if (pagamento.getIdFornecedor() != null) {
                uJComboBoxCorentista.setSelectedItem(pagamento.getIdFornecedor().getNomeCorentista() + " - " + pagamento.getIdFornecedor().getIdCliente().toString());
            }
            jTextFieldCorentista.setText(pagamento.getNomeFornecedor());
            if (pagamento.getSemCadastrado()) {
                jTextFieldCorentista.setVisible(true);
                uJComboBoxCorentista.setVisible(false);
            } else {
                jTextFieldCorentista.setVisible(false);
                uJComboBoxCorentista.setVisible(true);
            }
            jTextFieldOutrosNumeros.setText(pagamento.getOutrosNumeros());
            jLabel1CodigoPagamento.setText(pagamento.getIdPagamentos() == null ? "0" : pagamento.getIdPagamentos().toString());
            jTextFieldvalor.setText(ManagerDecimal.converter(pagamento.getValorTitulo(), "#,##0.00").replace(",", "").replace(".", ""));
            jTextFieldMulta.setText(ManagerDecimal.converter(pagamento.getMulta(), "#,##0.00").replace(",", "").replace(".", ""));
            jTextFieldJuros.setText(ManagerDecimal.converter(pagamento.getJuros(), "#,##0.00").replace(",", "").replace(".", ""));
            jTextFieldCorecao.setText(ManagerDecimal.converter(pagamento.getCorrecao(), "#,##0.00").replace(",", "").replace(".", ""));
            jTextAreaObs.setText(pagamento.getObs());
        }
    }
