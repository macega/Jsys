    private void loadTributos() {
        java.util.Collection listTributos = Retorna.findCollection("JsysProdutosTTributacao.findAll");
        for (Object o : listTributos) {
            JsysProdutosTTributacao t = (JsysProdutosTTributacao) o;
            uJComboBoxIdTributacao.addItem(t.getNome() + " - " + t.getDecreto(), t);
        }
        if (RegistroAtual.getIdTributacao() != null) {
            uJComboBoxIdTributacao.setSelectedItem(RegistroAtual.getIdTributacao().getNome() + " - " + RegistroAtual.getIdTributacao().getDecreto());
        }
    }

    @SuppressWarnings("unchecked")
    public void loadFabricantes() {
        // carega registros das Fabricantes
        try {
            br.sql.acesso.ConnectionFactory.iniciaBean();
            java.util.Collection data = jsysProdutosTFabricantesQuery.getResultList();
            for (Object entity : data) {
                br.sql.acesso.ConnectionFactory.getEntityManagerNew().refresh(entity);
            }
            jsysProdutosTFabricantesList.clear();
            jsysProdutosTFabricantesList.addAll(data);
            uJComboBoxFabricantes.clear();
            for (JsysProdutosTFabricantes fa : jsysProdutosTFabricantesList) {
                uJComboBoxFabricantes.addItem(fa.getNomefabricante(), fa);
            }
            uJComboBoxFabricantes.setSelectedItem(RegistroAtual.getIdFabricante().getNomefabricante());
        } catch (Exception e) {
            Log.registraErro(ProdutosEdit.class, "loadFabricantes", e);
        }
    }

