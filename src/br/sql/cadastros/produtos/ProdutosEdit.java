class OrigemFiscal {

    private final String id;
    private final String origemFiscal;

    public OrigemFiscal(String id, String origemFiscal) {
        this.id = id;
        this.origemFiscal = origemFiscal;
    }

    public String getId() {
        return id;
    }

    public String getOrigemFiscal() {
        return origemFiscal;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 97 * hash + Objects.hashCode(this.id);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final OrigemFiscal other = (OrigemFiscal) obj;
        return Objects.equals(this.id, other.id);
    }
}

