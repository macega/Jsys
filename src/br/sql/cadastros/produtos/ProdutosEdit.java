    private void registraCodigoBarra() {
        erroCodBarra.setText("");
        try {
            String codBarra = addCodigoBarras.getText().trim();
            if (!codBarra.equals("")) {
                Map<Object, Object> filtro = new HashMap<>();
                filtro.put("idbarra", codBarra);
                JsysProdutosTBarra B = (JsysProdutosTBarra) br.sql.util.Retorna.findOneResult("JsysProdutosTBarra.findByIdbarra", filtro);
                if (B == null) {
                    JsysProdutosTBarra b = new JsysProdutosTBarra(codBarra, idProduto);
                    br.sql.acesso.ConnectionFactory.insert(b);
                    retistroAtualJsysProdutosTBarra.add(b);
                    sons.pago();
                } else {
                    if (Objects.equals(B.getJsysProdutosT().getIdProduto(), idProduto)) {
                        sons.pago();
                        erroCodBarra.setForeground(Color.blue);
                    } else {
                        sons.naoEncontrado();
                        erroCodBarra.setForeground(Color.red);
                    }
                    // erroCodBarra.setForeground(Objects.equals(B.getJsysProdutosT().getIdProduto(), idProduto) ? Color.blue : Color.red);
                    erroCodBarra.setText("já Cadastrado: " + B.getJsysProdutosT().getIdProduto().toString() + " - " + B.getJsysProdutosT().getNomeProduto());
                }
            }
        } catch (Exception e) {
            br.sql.log.Log.registraErro(this.getClass().getName(), "registraCodigoBarra", e);
        } finally {
            int row = retistroAtualJsysProdutosTBarra.size() - 1;
            jTableBarras.setRowSelectionInterval(row, row);
            jTableBarras.scrollRectToVisible(jTableBarras.getCellRect(row, 0, true));
            addCodigoBarras.setText("");
            addCodigoBarras.requestFocus();
        }
    }
