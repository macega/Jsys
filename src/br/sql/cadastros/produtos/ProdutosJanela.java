    private String montaSQL() {
        String campos[] = {"jsysProdutosT.nomeProduto", "jsysProdutosT.idProduto", "jsysProdutosT.estoqueGeral", "jsysProdutosT.marca", "jsysProdutosTBarra.idbarra"};
        String order[] = {"jsysProdutosT.idProduto"};
        ManagerSQL sql = new ManagerSQL();
        sql.setTabela("jsysProdutosT LEFT JOIN jsysProdutosTBarra ON jsysProdutosT.idProduto = jsysProdutosTBarra.idProduto");
        sql.setCampos(campos);
        sql.setOrder(order);
        return sql.getSQL(consultaTF.getText());
    }

    private void actionConsulta(String sql) {
        try {
            ResultSet x = DADOS.execSQL(sql);
            // Definição do TableModel
            DefaultTableModel aModel = new DefaultTableModel(new Object[][]{}, new String[]{"NOME PRODUTO", "CODIGO", "ESTOQUE", "MARCA", "CODIGO BARRAS"}) {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };
            java.sql.ResultSetMetaData rsmd = x.getMetaData();
            int colNo = rsmd.getColumnCount();
            while (x.next()) {
                Object[] objects = new Object[colNo];
                for (int i = 0; i < colNo; i++) {
                    objects[i] = x.getObject(i + 1);
                }
                aModel.addRow(objects);
            }
            ListaProdutos.setModel(aModel);
            ListaProdutos.getColumnModel().getColumn(0).setPreferredWidth(450);
            ListaProdutos.setDefaultRenderer(Object.class, zebradoTableCellRenderer1);
        } catch (SQLException e) {
            Log.registraErro(this.getClass().getName(), "actionConsulta", e);
        }
    }

    private void editar() {
        int[] selected = ListaProdutos.getSelectedRows();
        for (int idx = 0; idx < selected.length; idx++) {
            JsysProdutosT prod = Retorna.jsysProdutoT((int) ListaProdutos.getValueAt(selected[idx], 1));
            ProdutosEdit pc = new ProdutosEdit(null, true, prod, this);
            pc.setVisible(true);
            loadProdutos();
        }
    }

