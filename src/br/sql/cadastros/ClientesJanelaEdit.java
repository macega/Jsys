    /**
     * carega as cidades do estado selecionado
     *
     * @param uf
     */
    private void setCidade() {
        Map<Object, Object> filtro = new HashMap<>();
        filtro.put("uf", jComboBoxUf.getSelectedItem().toString());
        List<Object> cidadeList = Retorna.findList("Cidades.findByUf", filtro);
        try {
            uJComboBoxCidade.clear();
            for (Object O : cidadeList) {
                Cidades c = (Cidades) O;
                uJComboBoxCidade.addItem(c.getMunicipio(), c);
            }
            if (RegistroJsysClientes.getCidade().equals("")) {
                uJComboBoxCidade.setSelectedIndex(0);
            } else {
                uJComboBoxCidade.setSelectedItem(RegistroJsysClientes.getCidade());
            }
        } catch (Exception e) {
            Log.registraErro(this.getClass().getName(), "setCidade", e);
        }
    }
    
    private void setLoja() {
        try {
            List<JsysLojas> lojaList = Retorna.jsysLojas();
            if (lojaList != null) {
                uJComboBoxLoja.clear();
                uJComboBoxLojaEmp.clear();
                for (JsysLojas l : lojaList) {
                    uJComboBoxLoja.addItem(l.getNomeLoja() + " - " + l.getIdloja(), l);
                    uJComboBoxLojaEmp.addItem(l.getNomeLoja() + " - " + l.getIdloja(), l);
                }
                if (RegistroJsysClientes.getIdLoja() == null) {
                    Map<Object, Object> filtro = new HashMap<>();
                    filtro.put("ativo", true);
                    JsysLojas jL = ((JsysLojas) Retorna.findOneResult("JsysLojas.findByAtivo", filtro));
                    uJComboBoxLoja.setSelectedItem(jL.getNomeLoja() + " - " + jL.getIdloja());
                    uJComboBoxLojaEmp.setSelectedItem(jL.getNomeLoja() + " - " + jL.getIdloja());
                } else {
                    JsysLojas loja = Retorna.jsysLojas(RegistroJsysClientes.getIdLoja());
                    uJComboBoxLoja.setSelectedItem(loja.getNomeLoja() + " - " + loja.getIdloja());
                    uJComboBoxLojaEmp.setSelectedItem(loja.getNomeLoja() + " - " + loja.getIdloja());
                }
            }
        } catch (Exception e) {
            Log.registraErro(this.getClass().getName(), "setLoja", e);
        }
    }

