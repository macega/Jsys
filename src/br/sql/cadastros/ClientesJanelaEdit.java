    private boolean validaDados() {
        if (Validar.data(admissaoDC.getDate()) == 0) {
            erro("Data de admissÃ£o invalida ");
            jTabbedPaneCadastro.setSelectedComponent(this.jPanelColaborador);
            admissaoDC.requestFocus();
            return false;
        }
        if (Validar.data(demissaoDC.getDate()) == 0) {
            erro("Data de demissÃ£o invalida ");
            jTabbedPaneCadastro.setSelectedComponent(this.jPanelColaborador);
            demissaoDC.requestFocus();
            return false;
        }
        if (Validar.data(exameAdmissionaDC.getDate()) == 0) {
            erro("Data de exame admissional invalida ");
            jTabbedPaneCadastro.setSelectedComponent(this.jPanelColaborador);
            demissaoDC.requestFocus();
            return false;
        }
        if (Validar.data(dataNascimentoJD.getDate()) == 0) {
            erro("Data de nascimento invalida ");
            dataNascimentoJD.requestFocus();
            return false;
        }
        if (Validar.data(dataCTPSDC.getDate()) == 0) {
            erro("Data de CTPS invalida ");
            jTabbedPaneCadastro.setSelectedComponent(this.jPanelColaborador);
            dataCTPSDC.requestFocus();
            return false;
        }
        if (Validar.data(pisDataDC.getDate()) == 0) {
            erro("Data do PIS invalida ");
            jTabbedPaneCadastro.setSelectedComponent(this.jPanelColaborador);
            pisDataDC.requestFocus();
            return false;
        }
        if (Validar.data(dataRGDC.getDate()) == 0) {
            erro("Data do RG invalida ");
            jTabbedPaneCadastro.setSelectedComponent(this.jPanelCliente);
            dataRGDC.requestFocus();
            return false;
        }
        if (!(getuJComboBoxCidade() instanceof Cidades)) {
            erro("Cidade NÃ£o Cadastrado ");
            jTabbedPaneCadastro.setSelectedComponent(this.jPanelGeral);
            uJComboBoxCidade.requestFocus();
            return false;
        }
        if (nomeTF.getText().trim().equals("")) {
            erro("Nome do Cliente Invalido ");
            nomeTF.requestFocus();
            return false;
        }
        if (fantasiaTF.getText().trim().equals("")) {
            erro("Fantasia nÃ£o pode ser vazia ");
            fantasiaTF.requestFocus();
            return false;
        }
        if (cepJTF.getText().trim().equals("-")) {
            erro("CEP Nao pode ser vazio ");
            jTabbedPaneCadastro.setSelectedComponent(this.jPanelGeral);
            cepJTF.requestFocus();
            return false;
        }
        if (logradouroTF.getText().trim().equals("")) {
            erro("Logradouro Nao pode ser vazio ");
            jTabbedPaneCadastro.setSelectedComponent(this.jPanelGeral);
            logradouroTF.requestFocus();
            return false;
        }
        if (numeroTF.getText().trim().equals("")) {
            erro("Numero Nao pode ser vazio ");
            jTabbedPaneCadastro.setSelectedComponent(this.jPanelGeral);
            numeroTF.requestFocus();
            return false;
        }
        if (bairroTF.getText().trim().equals("")) {
            erro("Bairro Nao pode ser vazio ");
            jTabbedPaneCadastro.setSelectedComponent(this.jPanelGeral);
            bairroTF.requestFocus();
            return false;
        }
        if (jRadioButtonJuridica.isSelected()) {
            if (!Validar.cpfCnpj(cnpjTF.getText())
                    & !"".equals(cnpjTF.getText())) {
                erro("CNPJ Invalido ");
                jTabbedPaneCadastro.setSelectedComponent(this.jPanelEmpresa);
                cnpjTF.requestFocus();
                return false;
            } else {
                Map<Object, Object> filtro = new HashMap<>();
                filtro.put("cnpjCpf", cnpjTF.getText());
                JsysClientes c = (JsysClientes) Retorna.findOneResult("JsysClientes.findByCnpjCpf", filtro);
                if (c != null && !"".equals(c.getCnpjCpf()) && !Objects.equals(c.getIdCliente(), RegistroJsysClientes.getIdCliente())) {
                    erro(new StringBuilder().append("CNPJ jÃ¡ Cadastrado para\n")
                            .append(c.getIdCliente()).append(" - [")
                            .append(c.getNomeCorentista()).append("].").toString());
                    jTabbedPaneCadastro.setSelectedComponent(this.jPanelEmpresa);
                    cnpjTF.requestFocus();
                    return false;
                }
            }
            if (!Validar.IE(jComboBoxUf.getSelectedItem().toString(), ieTF.getText()) | ieTF.getText().equals("")) {
                erro("IE Invalido ");
                jTabbedPaneCadastro.setSelectedComponent(this.jPanelEmpresa);
                ieTF.requestFocus();
                return false;
            }
        } else {
            if (!Validar.cpfCnpj(cpfTF.getText())
                    & !"".equals(cpfTF.getText())) {
                erro("CPF Invalido ");
                jTabbedPaneCadastro.setSelectedComponent(this.jPanelCliente);
                cpfTF.requestFocus();
                return false;
            } else {
                Map<Object, Object> filtro = new HashMap<>();
                filtro.put("cnpjCpf", cpfTF.getText());
                JsysClientes c = (JsysClientes) Retorna.findOneResult("JsysClientes.findByCnpjCpf", filtro);
                if (c != null && !"".equals(c.getCnpjCpf()) && !Objects.equals(c.getIdCliente(), RegistroJsysClientes.getIdCliente())) {
                    erro(new StringBuilder().append("CPF jÃ¡ Cadastrado para\n")
                            .append(c.getIdCliente()).append(" - [")
                            .append(c.getNomeCorentista()).append("].").toString());
                    jTabbedPaneCadastro.setSelectedComponent(this.jPanelCliente);
                    cpfTF.requestFocus();
                    return false;
                }
            }
        }
        if (codMunicipioTF.getText().equals("")) {
            erro("Cidade Invalido ");
            jTabbedPaneCadastro.setSelectedComponent(this.jPanelGeral);
            uJComboBoxCidade.requestFocus();
            return false;
        }
        if (Validar.data(avisoInicio.getDate()) == 1 & Validar.data(avisoFim.getDate()) == -1) {
            erro("Verifique as datas de aviso prÃ©vio do colaborador");
            jTabbedPaneCadastro.setSelectedComponent(this.jPanelColaborador);
            avisoInicio.requestFocus();
            return false;
        }
        return true;
    }
    
    private void setForm() {
        if (jRadioButtonJuridica.isSelected()) {
            dataNascimentoJD.setEnabled(false);
            sexoCB.setEnabled(false);
            jTabbedPaneCadastro.setEnabledAt(1, false);
            jTabbedPaneCadastro.setEnabledAt(3, true);
            colaboradorCB.setSelected(false);
            colaboradorCB.setEnabled(false);
            filialCB.setEnabled(true);
        } else {
            dataNascimentoJD.setEnabled(true);
            sexoCB.setEnabled(true);
            jTabbedPaneCadastro.setEnabledAt(1, clienteCB.isSelected());
            jTabbedPaneCadastro.setEnabledAt(3, false);
            colaboradorCB.setEnabled(true);
            filialCB.setSelected(false);
            filialCB.setEnabled(false);
        }
        jTabbedPaneCadastro.setEnabledAt(2, colaboradorCB.isSelected());
        jTabbedPaneCadastro.setSelectedComponent(this.jPanelGeral);
    }
