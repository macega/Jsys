    private void SalvaCorentista(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvaCorentista
        if (validaDados()) {
            setRegistroJsysClientes();
            this.ConfirmaClientes = true;
            if (RegistroJsysClientes.getIdCliente() != null) {
                RegistroJsysClientes.setUsuarioalteracao(Menu.getNomeUsuario());
                RegistroJsysClientes.setDataalteracao(ManagerData.getDate());
                br.sql.acesso.ConnectionFactory.update(RegistroJsysClientes);
            } else {
                RegistroJsysClientes.setUsuarioInclusao(Menu.getNomeUsuario());
                RegistroJsysClientes.setDataInclusao(ManagerData.getDate());
                br.sql.acesso.ConnectionFactory.insert(RegistroJsysClientes);
            }
            setVisible(false);
        }
    }//GEN-LAST:event_SalvaCorentista

