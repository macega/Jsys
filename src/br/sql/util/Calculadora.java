    private void JBIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBIgualActionPerformed
        num2 = Double.valueOf(JTVisor.getText());
        switch (aoperacao) {
            case '+':
                resultado = num1 + num2;
                JTVisor.setText(String.valueOf(resultado));
                break;
            case '-':
                resultado = num1 - num2;
                JTVisor.setText(String.valueOf(resultado));
                break;
            case '*':
                resultado = num1 * num2;
                JTVisor.setText(String.valueOf(resultado));
                break;
            case '/':
                if (num2 == 0) {
                    JTVisor.setText("Impossível dividir por 0.");
                } else {
                    resultado = num1 / num2;
                    JTVisor.setText(String.valueOf(resultado));
                }
                break;
        }
    }//GEN-LAST:event_JBIgualActionPerformed

