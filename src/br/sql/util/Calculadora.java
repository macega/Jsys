    private void JBMaisMenosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBMaisMenosActionPerformed
        if (temp.startsWith("-")) {
            temp.replace("-", "+");
        } else if (temp.startsWith("+")) {
            temp.replace("+", "-");
        } else {
            temp = "-" + temp;
        }
        JTVisor.setText(temp);
    }//GEN-LAST:event_JBMaisMenosActionPerformed

