    private void codigoTFKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_codigoTFKeyReleased
        Map<Object, Object> filtro = new HashMap<>();
        filtro.put("codigoBarra", codigoTF.getText().trim());
        br.sql.bean.views.JsysIdProdutos p = (br.sql.bean.views.JsysIdProdutos) br.sql.util.Retorna.findOneResult("JsysIdProdutos.findByCodigoBarra", filtro);
        if (p != null) {
            nomeProdutoTF.setText(p.getIdProduto().getNomeProduto());
        } else {
            nomeProdutoTF.setText("");
        }
    }//GEN-LAST:event_codigoTFKeyReleased

