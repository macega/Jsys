    private void actionConsulta() {
        try {
            String campos[] = {"Id", "Codigo", "Ex", "Tipo", "Descricao", "NacionalFederal", "ImportadosFederal", "Estadual", "Municipal", "Vigenciainicio", "Vigenciafim", "Chave", "Versao", "Fonte"};
            String order[] = {"descricao"};
            ManagerSQL sql = new ManagerSQL();
            sql.setTabela("NCM");
            sql.setCampos(campos);
            sql.setOrder(order);
            java.util.Map<Object, Object> onde = new java.util.HashMap<>();
            onde.put("NCM.tipo", "= 0");
            sql.setOnde(onde);
            ResultSet x = dados.execSQL(sql.getSQL(consultaTF.getText()));
            // Definição do TableModel
            DefaultTableModel aModel = new DefaultTableModel(new Object[][]{}, campos) {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };
            java.sql.ResultSetMetaData rsmd = x.getMetaData();
            int colNo = rsmd.getColumnCount();
            while (x.next()) {
                Object[] objects = new Object[colNo];
                for (int i = 0; i < colNo; i++) {
                    objects[i] = x.getObject(i + 1);
                }
                aModel.addRow(objects);
            }
            jTable1.setModel(aModel);
            jTable1.getColumnModel().getColumn(4).setPreferredWidth(450);
            jTable1.setDefaultRenderer(Object.class, new Zebrado());
        } catch (SQLException e) {
            Log.registraErro(this.getClass().getName(), "actionConsulta", e);
        }
    }

    private void selecionaTabela() {
        try {
            jTable1.requestFocus();
            jTable1.setRowSelectionInterval(0, 0);
        } catch (Exception ignore) {
        }
    }

    private String retorno;

    private void setRetorno() {
        this.retorno = String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), 1));
        this.dispose();
    }

    public String getRetorno() {
        return retorno;
    }
