package br.sql.bean.geral;

/**
 *
 * @author Juliano Alves Medina
 */
public class ReciboBean {

    private String recebeDe;
    private String endereco;
    private String quantiaDe;
    private String referenteA;
    private Double valor;
    private String emitente;
    private String cnpjCpf;
    private String parFantasia;
    private String parEndereco;
    private String parLogo;

    public ReciboBean() {
    }

    public String getRecebeDe() {
        return recebeDe;
    }

    public void setRecebeDe(String recebeDe) {
        this.recebeDe = recebeDe;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public String getQuantiaDe() {
        return quantiaDe;
    }

    public void setQuantiaDe(String quantiaDe) {
        this.quantiaDe = quantiaDe;
    }

    public String getReferenteA() {
        return referenteA;
    }

    public void setReferenteA(String referenteA) {
        this.referenteA = referenteA;
    }

    public Double getValor() {
        return valor;
    }

    public void setValor(Double valor) {
        this.valor = valor;
    }

    public String getEmitente() {
        return emitente;
    }

    public void setEmitente(String emitente) {
        this.emitente = emitente;
    }

    public String getCnpjCpf() {
        return cnpjCpf;
    }

    public void setCnpjCpf(String cnpjCpf) {
        this.cnpjCpf = cnpjCpf;
    }

    public String getParFantasia() {
        return parFantasia;
    }

    public void setParFantasia(String parFantasia) {
        this.parFantasia = parFantasia;
    }

    public String getParEndereco() {
        return parEndereco;
    }

    public void setParEndereco(String parEndereco) {
        this.parEndereco = parEndereco;
    }

    public String getParLogo() {
        return parLogo;
    }

    public void setParLogo(String parLogo) {
        this.parLogo = parLogo;
    }
    
    
}
