    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (validar()) {
            ref.setNfeId(registroAtual);
            if (jRadioButtonNfe.isSelected()) {
                ref.setNFeRef(true);
                ref.setRefNFe(jTextFieldchaveAcesso.getText().trim());
            } else if (jRadioButtonCte.isSelected()) {
                ref.setCTeRef(true);
                ref.setRefCTe(jTextFieldchaveAcesso.getText().trim());
            } else if (jRadioButtonNotaFiscal.isSelected()) {
                Map<Object, Object> filtro = new HashMap<>();
                filtro.put("uf", jComboBoxUf.getSelectedItem().toString());
                JsysUnidadeFederecao UF = (JsysUnidadeFederecao) Retorna.findOneResult("JsysUnidadeFederecao.findByUf", filtro);
                if (UF != null) {
                    ref.setNfRef(true);
                    ref.setCUF(UF.getIdUf());
                    ref.setAamm(ManagerString.RemoveFormato(jTextFieldAno.getText().trim()));
                    ref.setCnpj(ManagerString.RemoveFormato(jTextFieldCnpj.getText().trim()));
                    ref.setModelo(ManagerString.RemoveFormato(jTextFieldmodelo.getText().trim()));
                    ref.setSerie(ManagerString.RemoveFormato(jTextFieldSerie.getText().trim()));
                    ref.setNNF(ManagerString.RemoveFormato(jTextFieldnumero.getText().trim()));
                }
            }
            this.dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

