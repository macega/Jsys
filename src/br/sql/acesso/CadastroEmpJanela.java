    /**
     * Grava no arquivo .ini os dados da conexao com o banco de dados
     */
    public void setIni() {
        IniFiles ini = new IniFiles("Jsys.ini");
        if (nomeLojaTF.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Obrigat√≥rio preencher o campo NOME!");
        } else {
            String N[];
            int x = 0;
            if (ini.getString("LISTA BASE", "LISTA") == null) {
                ini.setString("LISTA BASE", "LISTA", nomeLojaTF.getText().toUpperCase());
            } else {
                if (ini.getString("LISTA BASE", "LISTA") != null) {
                    N = ini.getString("LISTA BASE", "LISTA").split(",");
                    for (String n : N) {
                        if (n.equals(nomeLojaTF.getText().toUpperCase())) {
                            x++;
                        }
                    }
                }
                if (x == 0) {
                    ini.setString("LISTA BASE", "LISTA", new StringBuilder()
                            .append(ini.getString("LISTA BASE", "LISTA"))
                            .append(",")
                            .append(nomeLojaTF.getText().toUpperCase()).toString());
                }
            }
            ini.setString("BASE " + nomeLojaTF.getText().toUpperCase(), "NOMESQLSERVER", nomeLojaTF.getText().toUpperCase());
            ini.setString("BASE " + nomeLojaTF.getText().toUpperCase(), "NOMEDRIVERSQLSERVER", nomeDriverSqlServerCB.getSelectedItem().toString());
            ini.setString("BASE " + nomeLojaTF.getText().toUpperCase(), "HOSTSQLSERVER", HostSqlServerTF.getText().toUpperCase());
            ini.setString("BASE " + nomeLojaTF.getText().toUpperCase(), "PORTASQLSERVER", portaSqlServerTF.getText());
            ini.setString("BASE " + nomeLojaTF.getText().toUpperCase(), "BANCO DE DADOSSQLSERVER", bancoDadosSqlServerTF.getText().toLowerCase());
            ini.setString("BASE " + nomeLojaTF.getText().toUpperCase(), "USUARIOSQLSERVER", usuarioSqlServerTF.getText());
            ini.setString("BASE " + nomeLojaTF.getText().toUpperCase(), "SENHASQLSERVER", String.valueOf(senhaSqlServerPF.getPassword()));
        }
        System.exit(0);
    }

